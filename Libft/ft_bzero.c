#include "libft.h"

void	ft_bzero(void *s, size_t n)
{	
	size_t			i;
	unsigned char	*uchar;

	i = 0;
	uchar = s;
	while (i < n)
	{
		*uchar++ = 0;
		i++;
	}
}
